"""
Шаг 3a: Self-Attention "на пальцах" — игрушечный пример.

Цель этого файла - НЕ обучить модель, а увидеть своими глазами,
как именно работает формула attention, на маленьких понятных числах,
прежде чем встраивать это в полноценную модель.
"""
import torch
import torch.nn as nn
from torch.nn import functional as F

torch.manual_seed(1337)

# --- Игрушечные данные ---
# Представим: у нас батч из 4 "предложений", в каждом по 8 "символов",
# и каждый символ уже представлен вектором из 32 чисел (embedding).
# Это НЕ реальные символы, просто случайные числа для демонстрации формул.
B, T, C = 4, 8, 32  # Batch, Time (длина последовательности), Channels (размер embedding)
x = torch.randn(B, T, C)

print(f"Входной тензор x: {x.shape}  (batch={B}, время={T}, embedding={C})")

# --- Шаг 1: наивное усреднение (baseline "для сравнения") ---
# Самый простой способ учесть контекст - усреднить текущий символ
# со всеми предыдущими. Проблема: все символы получают одинаковый вес.
xbow = torch.zeros((B, T, C))
for b in range(B):
    for t in range(T):
        x_prev = x[b, :t+1]      # все символы от начала до текущего (включительно)
        xbow[b, t] = x_prev.mean(dim=0)  # просто среднее

print(f"\nНаивное усреднение (bag-of-words): {xbow.shape}")
print("Проблема: символ 'забыл' и 'спешил' получат одинаковый вес при усреднении,")
print("хотя очевидно, что не все слова одинаково важны для предсказания.")

# --- Шаг 2: та же идея, но эффективно через матричное умножение ---
# Хитрость: нижнетреугольная матрица весов = взвешенное усреднение
# "текущий символ и всё, что было до него"
wei = torch.tril(torch.ones(T, T))
wei = wei / wei.sum(1, keepdim=True)
xbow2 = wei @ x  # (T,T) @ (B,T,C) -> (B,T,C), broadcast по батчу
print(f"\nТо же самое через матричное умножение: {xbow2.shape}")
print(f"Совпадает с наивным вариантом: {torch.allclose(xbow, xbow2, atol=1e-6)}")

# --- Шаг 3: НАСТОЯЩИЙ self-attention (одна голова) ---
head_size = 16

key = nn.Linear(C, head_size, bias=False)
query = nn.Linear(C, head_size, bias=False)
value = nn.Linear(C, head_size, bias=False)

k = key(x)    # (B, T, head_size)  - "что я предлагаю"
q = query(x)  # (B, T, head_size)  - "что я ищу"
v = value(x)  # (B, T, head_size)  - "что я реально передам"

# Оценка релевантности: query каждого символа сравниваем с key всех символов
wei = q @ k.transpose(-2, -1)  # (B,T,head_size) @ (B,head_size,T) -> (B,T,T)
print(f"\nМатрица 'сырых' оценок релевантности (до маскирования): {wei.shape}")
print("wei[0] (первый пример в батче) - строка t показывает оценки символа t")
print("ко всем остальным символам, включая будущие (это мы сейчас уберём):")
print(wei[0].detach().round(decimals=2))

# --- Маскирование: запрещаем "смотреть в будущее" ---
# Это критично для языковой модели: при предсказании символа №5
# модель не должна видеть символы №6, 7, 8 - иначе это подглядывание в ответ.
tril = torch.tril(torch.ones(T, T))
wei = wei.masked_fill(tril == 0, float('-inf'))  # будущее -> -бесконечность
wei = F.softmax(wei, dim=-1)  # -inf после softmax превращается в 0

print(f"\nПосле маскирования и softmax (веса в каждой строке суммируются в 1):")
print(wei[0].detach().round(decimals=2))

out = wei @ v  # (B,T,T) @ (B,T,head_size) -> (B,T,head_size)
print(f"\nИтоговый выход self-attention: {out.shape}")
print("Каждый символ теперь - это взвешенная сумма Value всех предыдущих")
print("символов, где веса зависят от того, насколько Query совпал с Key.")
